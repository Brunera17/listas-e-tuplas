
lista =[1,2,3]


"""
append()
    Adiciona um elemento ao final da lista
"""
lista.append(4)
print(lista)
"""
insert()
    Adiciona um elemento em uma posição especifica
    lista.insert(posição, valor)
"""
lista.insert(1,5)
print(lista)
"""
remove()
    Remove o primeiro elemento encontrado com o valor especificado
"""
lista.remove(2)
print(lista)
"""
sort()
    Ordena os elementos da lista em ordem crescente.
"""
lista.sort()
print(lista)
"""
reverse()
    Reverte a ordem dos elementos da lista
"""
lista.reverse()
print(lista)


""" 
pop()
    O método .pop() remove e retorna o elemento da posição indicada.
"""
print(lista.pop(1))
print(lista)